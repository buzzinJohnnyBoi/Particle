function drawSquareArt(startx, starty, array, rowLengthsize, size, type) {
  var x = 0;
  var y = array.length/rowLengthsize;
  for (var i = 0; i < array.length; i++) {
    x++;
    if ((i % rowLengthsize) == 0) {x = 0; y--;}

    if (array[i] == 1) {
      if (type == -1) {destroyblocks.push([startx + x*size, starty + y*size, size, 5, 5]);}
      else{platforms.Add(startx + x*size, starty + y*size, startx + x*size + size, starty + y*size + size, type);}
    }
    else if (array[i] == 0) {

    }
  }
}

